package util;

/**
 * Created by devinmcgloin on 8/17/15.
 * When comparing counts be sure to specify that the unit its a count.
 */
public class Expression {
        final String type;
        final String operator;
        final String value;
        final String unit;

        public Expression(String type, String operator, String value, String unit) {
            this.type = type;
            this.operator = operator;
            this.value = value;
            this.unit = unit;
        }

        @Override
        public String toString() {
            return "Expression{" +
                    "type='" + type + '\'' +
                    ", operator='" + operator + '\'' +
                    ", value='" + value + '\'' +
                    ", unit='" + unit + '\'' +
                    '}';
        }

        public String getType() {
            return type;
        }

        public String getOperator() {
            return operator;
        }

        public String getValue() {
            return value;
        }

        public String getUnit() {
            return unit;
        }

}

